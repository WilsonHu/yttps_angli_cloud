package com.eservice.iot.model;

public class Branch {
    /**
     * 机构ID
     */
    private String id;
    /**
     * 机构名称
     */
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
